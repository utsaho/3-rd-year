package com.medBuddy;

public class DeptInfo
{
    String deptName, deptHead, departmentId, deptContact, deptEmail;

    public DeptInfo(String deptName, String deptHead, String departmentId, String deptContact, String deptEmail)
    {
        this.deptName = deptName;
        this.deptHead = deptHead;
        this.departmentId = departmentId;
        this.deptContact = deptContact;
        this.deptEmail = deptEmail;
    }
}
